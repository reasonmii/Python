'''
1) head
Returns the head of a list

- Use lst[0] to return the first element of the passed list

https://www.30secondsofcode.org/python/s/head
'''

def head(lst):
    return lst[0]


# Example
head([1, 2, 3]) # 1


'''
2) last
Returns the last element in a list

- Use lst[-1] to return the last element of the passed list

https://www.30secondsofcode.org/python/s/last
'''

def last(lst):
    return lst[-1]


# Example
last([1, 2, 3]) # 3
